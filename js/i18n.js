  function getLang() {
    return localStorage.getItem('lang') ||
      (navigator.language?.toLowerCase().startsWith('th') ? 'th' : 'en');
  }

  function t(key) {
    const lang = getLang();
    return T[lang]?.[key] ?? T.th[key] ?? key;
  }

  function applyI18n() {
    const lang = getLang();
    const dict = T[lang] ?? T.th;
    document.documentElement.lang = lang;

    document.querySelectorAll('[data-i18n]').forEach(el => {
      const v = dict[el.dataset.i18n];
      if (v !== undefined) el.textContent = v;
    });
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
      const v = dict[el.dataset.i18nHtml];
      if (v !== undefined) el.innerHTML = v;
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      const v = dict[el.dataset.i18nPlaceholder];
      if (v !== undefined) el.placeholder = v;
    });
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
      const v = dict[el.dataset.i18nTitle];
      if (v !== undefined) document.title = v;
    });

    // sync lang switcher buttons
    document.querySelectorAll('.lang-btn').forEach(btn => {
      btn.classList.toggle('active', btn.dataset.lang === lang);
    });
  }

  window.t = t;
  window.getLang = getLang;
  window.applyI18n = applyI18n;
  window.setLang = function (lang) {
    localStorage.setItem('lang', lang);
    applyI18n();
  };
